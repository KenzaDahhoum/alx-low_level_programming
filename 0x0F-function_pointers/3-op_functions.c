#include "3-calc.h"

/**
  *op_add - add two integers
  *@a: first integer
  *@b: second integer
  *Return: sum
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - substract two integers
 *@a: first integer
 *@b: second integer
 *Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  *op_mul - multplie two integer
  *@a: first int
  *@b: second int
  *Return: product
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  *op_div - divides two integers
  *@a: first int
  *@b: second int
  *Return: division
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  *op_mod - modulo two integers
  *@a: first integer
  *@b: second integer
  *Return: remainder of the division
  */
int op_mod(int a, int b)
{

	return (a % b);
}
